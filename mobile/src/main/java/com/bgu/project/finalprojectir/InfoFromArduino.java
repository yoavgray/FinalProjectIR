public class InfoFromArduino {

    private String type;
    private String brand;

    public InfoFromArduino() {
    }

    public void setType(String type) {
        this.type = type;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getType() {
        return this.type;
    }

    public String getBrand() {
        return this.brand;
    }

}
